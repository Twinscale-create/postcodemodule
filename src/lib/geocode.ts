const cache = new Map<string, [number, number]>();

/** Synchrone lookup — alleen hardcoded postcodes, geen API-call */
export function getCoordsSync(postcode: string): [number, number] | null {
  return KNOWN_COORDS[postcode] ?? null
}

export async function getPostcodeCoords(postcode: string): Promise<[number, number]> {
  if (cache.has(postcode)) return cache.get(postcode)!;

  if (KNOWN_COORDS[postcode]) {
    cache.set(postcode, KNOWN_COORDS[postcode]);
    return KNOWN_COORDS[postcode];
  }

  // Fallback: use Nominatim geocoding (free, no API key needed)
  try {
    const response = await fetch(
      `https://nominatim.openstreetmap.org/search?q=${postcode},Netherlands&format=json&limit=1`,
      { headers: { 'User-Agent': 'TwinScale Dashboard/1.0' } }
    );
    const data = await response.json();
    if (data.length > 0) {
      const coords: [number, number] = [parseFloat(data[0].lat), parseFloat(data[0].lon)];
      cache.set(postcode, coords);
      return coords;
    }
  } catch {
    console.warn('Geocoding failed for postcode:', postcode);
  }

  // Default: center of the Netherlands
  return [52.1326, 5.2913];
}
